def collect_ordered_logit_irt_predictions(
    rubric_path: Path,
    train_task_ids: List[str],
    ground_truth_b: np.ndarray,
    train_responses: Dict[str, Dict[str, int]],
    baseline_abilities_df: "pd.DataFrame",
    all_task_ids: List[str],
    frontier_task_ids: List[str],
    verbose: bool = True,
    device: str = "cpu",
) -> Dict[str, Dict[str, float]]:
    """Train OrderedLogitIRTPredictor and collect predictions.

    Args:
        rubric_path: Path to raw rubric features CSV
        train_task_ids: Task IDs with baseline IRT difficulties
        ground_truth_b: Baseline IRT difficulties
        train_responses: Pre-frontier responses (not used, for API compatibility)
        baseline_abilities_df: DataFrame with 'theta' column, indexed by agent_id
        all_task_ids: All task IDs to predict for
        frontier_task_ids: Frontier task IDs (for computing prior mean)
        verbose: Print progress
        device: PyTorch device ("cpu" or "cuda")

    Returns:
        Dict with single key "Ordered Logit IRT" -> predictions dict

    Note:
        Predictions are only generated for tasks with rubric data. Tasks without
        rubric data will raise an error (no silent fallbacks).
    """
    import traceback

    import pandas as pd

    predictions: Dict[str, Dict[str, float]] = {}

    if not rubric_path.exists():
        print(f"\nRubric data not found: {rubric_path}")
        return predictions

    print(f"\nTraining Ordered Logit IRT...")

    try:
        predictor = OrderedLogitIRTPredictor(
            rubric_path=rubric_path,
            l2_discriminativeness=0.1,
            prior_strength=1.0,
            device=device,
            verbose=verbose,
        )

        baseline_agent_ids = list(baseline_abilities_df.index)
        baseline_theta = baseline_abilities_df["theta"].values

        predictor.fit(
            task_ids=train_task_ids,
            ground_truth_b=ground_truth_b,
            responses=train_responses,
            baseline_abilities=baseline_theta,
            baseline_agent_ids=baseline_agent_ids,
            frontier_task_ids=frontier_task_ids,
        )

        # Only predict for tasks that have rubric data
        rubric_task_ids = predictor._rubric_source.task_ids
        tasks_to_predict = [t for t in all_task_ids if t in rubric_task_ids]

        if verbose:
            print(f"   Predicting for {len(tasks_to_predict)} tasks with rubric data")
            skipped = len(all_task_ids) - len(tasks_to_predict)
            if skipped > 0:
                print(f"   Skipping {skipped} tasks without rubric data")

        preds = predictor.predict(tasks_to_predict)
        predictions["Ordered Logit IRT"] = preds

        # Print diagnostics
        diag = predictor.get_training_diagnostics()
        if verbose:
            print(f"   Trained on {diag['n_training_obs']} observations")
            print(f"   Prior: N({diag['prior_mean']:.3f}, {diag['prior_std']:.3f})")

    except Exception as e:
        print(f"  Error: {e}")
        traceback.print_exc()

    return predictions
