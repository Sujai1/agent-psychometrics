"""Ordered Logit IRT Predictor for frontier task difficulty prediction.

This module implements the Ordered Logit IRT approach that uses trajectory
rubric scores as an auxiliary output to predict frontier task difficulties.

The key insight is that rubric scores are generated by the latent task difficulty,
so we can invert the generative model to estimate difficulty from observed scores.
"""

from dataclasses import dataclass
from pathlib import Path
from typing import Any, Dict, List, Optional

import numpy as np


class OrderedLogitLayer:
    """Ordered logit model for a single rubric item with K levels.

    Model: P(Y = k | eta) = F(tau_{k+1} - linear) - F(tau_k - linear)
    where linear = lambda * eta + delta

    Parameters:
        - lambda (discriminativeness): how sensitive rubric is to latent ease
        - delta (bias): shifts the response curve
        - tau_1, ..., tau_{K-1} (thresholds): cutpoints for ordinal categories

    Uses cumulative parameterization to ensure tau_1 < tau_2 < ... < tau_{K-1}.
    """

    def __init__(self, n_levels: int = 6, device: str = "cpu"):
        """Initialize ordered logit layer.

        Args:
            n_levels: Number of ordinal levels (default 6 for 0-5 scale)
            device: PyTorch device
        """
        import torch
        import torch.nn.functional as F

        self.n_levels = n_levels
        self.n_thresholds = n_levels - 1
        self.device = device

        # Initialize thresholds to be roughly evenly spaced
        init_spacing = 4.0 / self.n_thresholds
        self.raw_thresholds = torch.full(
            (self.n_thresholds,),
            np.log(np.exp(init_spacing) - 1),
            dtype=torch.float32,
            device=device,
            requires_grad=True,
        )

        # Discriminativeness (lambda_k) - initialized positive
        self.log_discriminativeness = torch.tensor(
            0.0, dtype=torch.float32, device=device, requires_grad=True
        )

        # Bias (delta_k)
        self.bias = torch.tensor(
            0.0, dtype=torch.float32, device=device, requires_grad=True
        )

    @property
    def discriminativeness(self):
        """Get positive discriminativeness."""
        import torch

        return torch.exp(self.log_discriminativeness)

    def get_thresholds(self):
        """Get ordered thresholds via cumulative softplus.

        Returns:
            Tensor of shape (n_thresholds,) with tau_1 < tau_2 < ... < tau_{K-1}
        """
        import torch
        import torch.nn.functional as F

        # Softplus ensures positive deltas
        deltas = F.softplus(self.raw_thresholds)
        # Cumulative sum gives ordered sequence
        thresholds = torch.cumsum(deltas, dim=0)
        # Center around 0 for numerical stability
        thresholds = thresholds - thresholds.mean()
        return thresholds

    def forward(self, eta):
        """Compute category probabilities.

        Args:
            eta: Tensor of shape (batch_size,) - latent ease values (theta - beta)

        Returns:
            Tensor of shape (batch_size, n_levels) - probabilities P(Y=k|eta)
        """
        import torch

        thresholds = self.get_thresholds()  # (n_thresholds,)
        linear = self.discriminativeness * eta + self.bias  # (batch_size,)

        # Compute cumulative probabilities F(tau_k - linear)
        # Shape: (batch_size, n_thresholds)
        cum_probs = torch.sigmoid(thresholds.unsqueeze(0) - linear.unsqueeze(1))

        # P(Y=k) = F(tau_{k+1}) - F(tau_k)
        # Add boundaries: F(tau_0) = 0, F(tau_K) = 1
        zeros = torch.zeros(eta.shape[0], 1, device=eta.device)
        ones = torch.ones(eta.shape[0], 1, device=eta.device)

        # Shape: (batch_size, n_levels + 1)
        all_cum = torch.cat([zeros, cum_probs, ones], dim=1)

        # Category probabilities
        probs = all_cum[:, 1:] - all_cum[:, :-1]

        # Clamp for numerical stability
        probs = torch.clamp(probs, min=1e-8)
        probs = probs / probs.sum(dim=1, keepdim=True)

        return probs

    def log_prob(self, eta, y):
        """Compute log P(Y = y | eta).

        Args:
            eta: Tensor of shape (batch_size,) - latent ease values
            y: Tensor of shape (batch_size,) - observed categories (0 to n_levels-1)

        Returns:
            Tensor of shape (batch_size,) - log probabilities
        """
        import torch

        probs = self.forward(eta)  # (batch_size, n_levels)
        # Gather probabilities for observed categories
        log_probs = torch.log(probs.gather(1, y.unsqueeze(1)).squeeze(1))
        return log_probs

    def parameters(self):
        """Return list of trainable parameters."""
        return [self.raw_thresholds, self.log_discriminativeness, self.bias]


class MultiItemOrderedLogit:
    """Ordered logit model for multiple rubric items."""

    def __init__(self, rubric_items: List[str], n_levels: int = 6, device: str = "cpu"):
        """Initialize multi-item ordered logit model.

        Args:
            rubric_items: List of rubric item names
            n_levels: Number of ordinal levels per item
            device: PyTorch device
        """
        self.rubric_items = rubric_items
        self.n_levels = n_levels
        self.device = device

        self.layers = {
            item: OrderedLogitLayer(n_levels, device) for item in rubric_items
        }

    def total_log_likelihood(
        self,
        eta,  # (n_obs,)
        rubric_scores: Dict[str, "torch.Tensor"],  # item -> (n_obs,)
    ):
        """Compute total log-likelihood across all items.

        Args:
            eta: Latent ease values for each observation
            rubric_scores: Dict mapping item name to score tensor

        Returns:
            Scalar total log-likelihood
        """
        import torch

        total_ll = torch.tensor(0.0, device=self.device)

        for item, layer in self.layers.items():
            if item in rubric_scores:
                ll = layer.log_prob(eta, rubric_scores[item])
                total_ll = total_ll + ll.sum()

        return total_ll

    def parameters(self):
        """Return list of all trainable parameters."""
        params = []
        for layer in self.layers.values():
            params.extend(layer.parameters())
        return params

    def get_params_dict(self) -> Dict[str, Dict[str, float]]:
        """Get learned parameters for all items."""
        return {
            item: {
                "discriminativeness": layer.discriminativeness.item(),
                "bias": layer.bias.item(),
                "thresholds": layer.get_thresholds().detach().cpu().tolist(),
            }
            for item, layer in self.layers.items()
        }


class OrderedLogitIRTPredictor:
    """IRT-based difficulty prediction using rubric scores as auxiliary outputs.

    This predictor models rubric scores as generated by latent task difficulty:

    Phase 1 (provided): Baseline IRT gives theta_j (ability) and beta_i (difficulty)
    Phase 2 (fit): Learn ordered logit params from rubric ~ OrderedLogit(lambda * eta + delta)
    Phase 3 (predict): MAP estimate of beta_i for frontier tasks

    The model is:
        eta_ij = theta_j - beta_i  (latent "ease")
        rubric_ijk ~ OrderedLogit(lambda_k * eta_ij + delta_k; tau_k)

    For frontier tasks, we fix all parameters except beta_i and find the MAP estimate.
    Prior: beta_i ~ N(mean of frontier task difficulties, std of all task difficulties)

    STRICT DATA REQUIREMENTS:
    - Every task must have rubric data (exactly 6 agents)
    - Every rubric agent must have a baseline ability
    - No fallbacks - errors are raised for missing data
    """

    EXPECTED_AGENTS_PER_TASK = 6

    def __init__(
        self,
        rubric_path: Path,
        l2_discriminativeness: float = 0.1,
        prior_strength: float = 1.0,
        lr: float = 0.1,
        max_iter_fit: int = 500,
        max_iter_map: int = 100,
        tol: float = 1e-5,
        device: str = "cpu",
        verbose: bool = True,
    ):
        """Initialize Ordered Logit IRT predictor.

        Args:
            rubric_path: Path to raw rubric features CSV
            l2_discriminativeness: L2 regularization on lambda (prevents extreme values)
            prior_strength: Weight on N(mu, sigma^2) prior for MAP estimation
            lr: Learning rate for L-BFGS
            max_iter_fit: Max iterations for Phase 2 (parameter learning)
            max_iter_map: Max iterations for Phase 3 (MAP per task)
            tol: Convergence tolerance
            device: "cpu" or "cuda"
            verbose: Print progress
        """
        from experiment_appendix_h_hard_tasks.shared.rubric_preprocessing import (
            RubricDataSource,
            RubricPreprocessor,
        )

        self.rubric_path = Path(rubric_path)
        self.l2_discriminativeness = l2_discriminativeness
        self.prior_strength = prior_strength
        self.lr = lr
        self.max_iter_fit = max_iter_fit
        self.max_iter_map = max_iter_map
        self.tol = tol
        self.device = device
        self.verbose = verbose

        # Load rubric data
        self._rubric_source = RubricDataSource(self.rubric_path, RubricPreprocessor())

        # Model state (set after fit)
        self._ordered_logit: Optional[MultiItemOrderedLogit] = None
        self._baseline_abilities: Optional[Dict[str, float]] = None
        self._baseline_difficulties: Optional[Dict[str, float]] = None
        self._prior_mean: Optional[float] = None
        self._prior_std: Optional[float] = None
        self._is_fitted: bool = False

        # Diagnostics
        self._fit_loss_history: List[float] = []
        self._n_training_obs: int = 0

    @property
    def name(self) -> str:
        return "Ordered Logit IRT"

    def fit(
        self,
        task_ids: List[str],
        ground_truth_b: np.ndarray,
        responses: Dict[str, Dict[str, int]],
        baseline_abilities: Optional[np.ndarray] = None,
        baseline_agent_ids: Optional[List[str]] = None,
        frontier_task_ids: Optional[List[str]] = None,
    ) -> None:
        """Fit ordered logit parameters on pre-frontier data.

        Args:
            task_ids: All task IDs (for computing eta = theta - beta)
            ground_truth_b: Baseline IRT difficulties (indexed by task_ids)
            responses: Pre-frontier response matrix (not used directly, for API compat)
            baseline_abilities: Baseline IRT abilities array
            baseline_agent_ids: Agent IDs corresponding to baseline_abilities
            frontier_task_ids: Frontier task IDs (for computing prior mean)

        Raises:
            ValueError: If required data is missing or incomplete
        """
        import torch

        if baseline_abilities is None or baseline_agent_ids is None:
            raise ValueError("baseline_abilities and baseline_agent_ids required")

        if frontier_task_ids is None:
            raise ValueError(
                "frontier_task_ids required to compute prior mean for MAP estimation"
            )

        # Store baseline parameters
        self._baseline_abilities = dict(zip(baseline_agent_ids, baseline_abilities))
        self._baseline_difficulties = dict(zip(task_ids, ground_truth_b))

        # Verify all rubric agents have baseline abilities
        missing_agents = [
            a for a in self._rubric_source.agent_ids if a not in self._baseline_abilities
        ]
        if missing_agents:
            raise ValueError(
                f"Rubric agents missing from baseline abilities: {missing_agents}"
            )

        # Compute prior for MAP estimation:
        # - Mean: mean of FRONTIER task baseline difficulties
        # - Std: std of ALL task baseline difficulties (to capture scale)
        frontier_difficulties = [
            self._baseline_difficulties[t]
            for t in frontier_task_ids
            if t in self._baseline_difficulties
        ]
        if len(frontier_difficulties) == 0:
            raise ValueError("No frontier tasks found in baseline difficulties")

        self._prior_mean = float(np.mean(frontier_difficulties))
        self._prior_std = float(np.std(ground_truth_b))  # std of ALL tasks

        if self.verbose:
            print(
                f"   Ordered Logit IRT: Prior N({self._prior_mean:.3f}, {self._prior_std:.3f}^2)"
            )
            print(f"   Prior mean from {len(frontier_difficulties)} frontier tasks")

        # Get rubric observations
        obs_task_ids, obs_agent_ids, rubric_scores = (
            self._rubric_source.get_all_observations()
        )

        # Compute eta for all observations - all should be valid
        eta_values = []
        for t, a in zip(obs_task_ids, obs_agent_ids):
            if t not in self._baseline_difficulties:
                raise ValueError(f"Task '{t}' not found in baseline difficulties")
            if a not in self._baseline_abilities:
                raise ValueError(f"Agent '{a}' not found in baseline abilities")
            eta = self._baseline_abilities[a] - self._baseline_difficulties[t]
            eta_values.append(eta)

        self._n_training_obs = len(eta_values)

        if self.verbose:
            print(f"   Training on {self._n_training_obs} observations")

        # Convert to tensors
        eta_tensor = torch.tensor(eta_values, dtype=torch.float32, device=self.device)

        rubric_tensors = {}
        for item, scores in rubric_scores.items():
            rubric_tensors[item] = torch.tensor(
                scores, dtype=torch.int64, device=self.device
            )

        # Initialize model
        self._ordered_logit = MultiItemOrderedLogit(
            self._rubric_source.rubric_items, n_levels=6, device=self.device
        )

        # Optimize via L-BFGS
        params = self._ordered_logit.parameters()
        optimizer = torch.optim.LBFGS(
            params,
            lr=self.lr,
            max_iter=20,
            history_size=50,
            line_search_fn="strong_wolfe",
        )

        self._fit_loss_history = []

        def closure():
            optimizer.zero_grad()

            # Log-likelihood
            ll = self._ordered_logit.total_log_likelihood(eta_tensor, rubric_tensors)

            # L2 regularization on discriminativeness
            reg = torch.tensor(0.0, device=self.device)
            for layer in self._ordered_logit.layers.values():
                reg = reg + self.l2_discriminativeness * (layer.discriminativeness**2)

            loss = -ll + reg
            loss.backward()

            self._fit_loss_history.append(loss.item())
            return loss

        if self.verbose:
            print(f"   Fitting ordered logit parameters...")

        prev_loss = float("inf")
        for iteration in range(self.max_iter_fit):
            loss = optimizer.step(closure)

            if len(self._fit_loss_history) > 1:
                d_loss = abs(prev_loss - loss.item())
                if self.verbose and (iteration % 50 == 0 or iteration < 5):
                    print(
                        f"     Iter {iteration}: loss={loss.item():.4f}, d_loss={d_loss:.2e}"
                    )
                if d_loss < self.tol:
                    if self.verbose:
                        print(f"   Converged at iteration {iteration}")
                    break
                prev_loss = loss.item()

        self._is_fitted = True

        if self.verbose:
            print(f"   Final loss: {self._fit_loss_history[-1]:.4f}")
            params_dict = self._ordered_logit.get_params_dict()
            print("   Learned discriminativeness (lambda):")
            for item, p in params_dict.items():
                print(f"     {item}: {p['discriminativeness']:.3f}")

    def predict(self, task_ids: List[str]) -> Dict[str, float]:
        """Predict difficulty for tasks via batched MAP estimation.

        Optimizes all task betas simultaneously for GPU efficiency.
        For each task, finds beta_i that maximizes:
            log P(rubric | theta, beta_i, params) + log P(beta_i | prior)

        Args:
            task_ids: Tasks to predict difficulty for

        Returns:
            Dict mapping task_id -> predicted difficulty

        Raises:
            RuntimeError: If predictor is not fitted
            ValueError: If any task is missing rubric data or has wrong number of agents
        """
        import torch

        if not self._is_fitted:
            raise RuntimeError("Must call fit() before predict()")

        n_tasks = len(task_ids)
        n_agents = self.EXPECTED_AGENTS_PER_TASK

        # Pre-validate all tasks and collect data
        all_theta = []  # Will be (n_tasks, n_agents)
        all_scores = {item: [] for item in self._rubric_source.rubric_items}

        for task_id in task_ids:
            # Verify task has rubric data
            if task_id not in self._rubric_source.task_ids:
                raise ValueError(f"Task '{task_id}' not found in rubric data")

            # Get agents with rubric data for this task
            rubric_agent_ids, rubric_scores = self._rubric_source.get_scores_for_task(
                task_id
            )

            # Verify exactly 6 agents
            if len(rubric_agent_ids) != n_agents:
                raise ValueError(
                    f"Task '{task_id}' has {len(rubric_agent_ids)} rubric agents, "
                    f"expected {n_agents}"
                )

            # Verify all agents have baseline abilities
            missing_agents = [
                a for a in rubric_agent_ids if a not in self._baseline_abilities
            ]
            if missing_agents:
                raise ValueError(
                    f"Task '{task_id}' has rubric agents missing from baseline abilities: "
                    f"{missing_agents}"
                )

            # Collect abilities for this task
            theta_values = [self._baseline_abilities[a] for a in rubric_agent_ids]
            all_theta.append(theta_values)

            # Collect rubric scores
            for item in self._rubric_source.rubric_items:
                all_scores[item].append(rubric_scores[item])

        # Convert to tensors: theta (n_tasks, n_agents), scores (n_tasks, n_agents)
        theta_tensor = torch.tensor(
            all_theta, dtype=torch.float32, device=self.device
        )  # (n_tasks, n_agents)

        score_tensors = {
            item: torch.tensor(
                np.stack(scores), dtype=torch.int64, device=self.device
            )  # (n_tasks, n_agents)
            for item, scores in all_scores.items()
        }

        # Batched MAP estimation
        if self.verbose:
            print(f"   Running batched MAP estimation for {n_tasks} tasks...")

        beta_values = self._estimate_map_batched(theta_tensor, score_tensors)

        # Build result dict
        predictions = {
            task_id: beta_values[i] for i, task_id in enumerate(task_ids)
        }

        return predictions

    def _estimate_map_batched(
        self,
        theta: "torch.Tensor",  # (n_tasks, n_agents)
        rubric_scores: Dict[str, "torch.Tensor"],  # item -> (n_tasks, n_agents)
    ) -> List[float]:
        """Batched MAP estimation for all tasks simultaneously.

        Args:
            theta: Abilities tensor of shape (n_tasks, n_agents)
            rubric_scores: Dict of item -> scores tensor of shape (n_tasks, n_agents)

        Returns:
            List of predicted beta values for each task
        """
        import torch

        n_tasks = theta.shape[0]

        # Initialize all betas at prior mean
        betas = torch.full(
            (n_tasks,),
            self._prior_mean,
            dtype=torch.float32,
            device=self.device,
            requires_grad=True,
        )

        optimizer = torch.optim.LBFGS(
            [betas],
            lr=self.lr,
            max_iter=20,
            history_size=50,
            line_search_fn="strong_wolfe",
        )

        def closure():
            optimizer.zero_grad()

            # eta = theta - beta (broadcast: (n_tasks, n_agents) - (n_tasks, 1))
            eta = theta - betas.unsqueeze(1)  # (n_tasks, n_agents)

            # Compute log-likelihood for all tasks
            total_ll = torch.tensor(0.0, device=self.device)

            for item, layer in self._ordered_logit.layers.items():
                if item in rubric_scores:
                    # Flatten for batch processing
                    eta_flat = eta.reshape(-1)  # (n_tasks * n_agents,)
                    scores_flat = rubric_scores[item].reshape(-1)  # (n_tasks * n_agents,)

                    # Get log probs
                    ll = layer.log_prob(eta_flat, scores_flat)
                    total_ll = total_ll + ll.sum()

            # Log-prior for all betas: N(prior_mean, prior_std^2)
            log_prior = (
                -0.5
                * self.prior_strength
                * ((betas - self._prior_mean) / self._prior_std) ** 2
            ).sum()

            # Negative posterior (for minimization)
            loss = -(total_ll + log_prior)
            loss.backward()
            return loss

        prev_loss = float("inf")
        for iteration in range(self.max_iter_map):
            loss = optimizer.step(closure)

            # Check convergence
            d_loss = abs(prev_loss - loss.item())
            if d_loss < self.tol:
                if self.verbose:
                    print(f"     MAP converged at iteration {iteration}")
                break
            prev_loss = loss.item()

        return betas.detach().cpu().tolist()

    def get_training_diagnostics(self) -> Dict[str, Any]:
        """Return diagnostics from parameter learning phase."""
        if not self._is_fitted:
            return {"is_fitted": False}

        return {
            "is_fitted": True,
            "n_training_obs": self._n_training_obs,
            "final_loss": self._fit_loss_history[-1] if self._fit_loss_history else None,
            "loss_history": self._fit_loss_history,
            "prior_mean": self._prior_mean,
            "prior_std": self._prior_std,
            "ordered_logit_params": self._ordered_logit.get_params_dict(),
        }


